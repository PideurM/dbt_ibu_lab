SELECT
    event_id,
    event_description,
    short_description,
    nat,
    level,
    race_id,
    race_description,
    {{ clear_km_relays('km') }},
    cat_id,
    discipline_id,
    start_time,
    status_text,
    ibu_id,
    name,
    short_name,
    family_name,
    given_name,
    athlete_nat,
    is_team,
    start_order,
    result_order,
    irm,
    bib,
    leg,
    rank,
    {{ select_spares('shootings') }},
    CASE WHEN POSITION('+' IN shooting_total) > 0
        THEN LEFT(shooting_total, POSITION('+' IN shooting_total) - 1)::INT + RIGHT(shooting_total, LENGTH(shooting_total) - POSITION('+' IN shooting_total))::INT
        ELSE shooting_total::INT
    END AS shooting_total,
    run_time,
    total_time,
    wc,
    nc,
    noc,
    behind,
    start_group,
    team_id,
    pursuit_start_distance,
    result
FROM {{ ref('stg_race_results') }}
